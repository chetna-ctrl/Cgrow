-- ============================================================
-- 4. BRAINS & AUTOMATION (Triggers)
-- ============================================================

-- Function: Auto-update Timestamp
CREATE OR REPLACE FUNCTION update_updated_at_column() RETURNS TRIGGER AS $$
BEGIN NEW.updated_at = now(); RETURN NEW; END; $$ LANGUAGE plpgsql;

CREATE TRIGGER update_user_settings_updated_at BEFORE UPDATE ON public.user_settings FOR EACH ROW EXECUTE FUNCTION update_updated_at_column();

-- Function: Blackout Logic
CREATE OR REPLACE FUNCTION calculate_blackout_dates() RETURNS TRIGGER AS $$
BEGIN
  IF NEW.sow_date IS NOT NULL THEN
    NEW.blackout_end_date := NEW.sow_date + INTERVAL '3 days';
    NEW.is_in_blackout := (CURRENT_DATE <= (NEW.sow_date + INTERVAL '3 days'));
  END IF;
  RETURN NEW;
END; $$ LANGUAGE plpgsql;

CREATE TRIGGER set_blackout_dates BEFORE INSERT OR UPDATE ON public.batches FOR EACH ROW EXECUTE FUNCTION calculate_blackout_dates();

